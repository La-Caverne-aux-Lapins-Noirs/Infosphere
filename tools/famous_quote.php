function famous_quote($id = -1)
{
    global $FamousQuote;
    global $Language;

    if (!isset($FamousQuote[$Language]))
	return ("");
    $fq = $FamousQuote[$Language];
    if ($id != -1)
	$fq = $fq[$id % count($fq)];
    else
	$fq = $fq[rand(0, count($fq) - 1)];
    return ($fq);
}
